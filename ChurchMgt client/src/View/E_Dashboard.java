    private void displayTablMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_displayTablMouseClicked
        DefaultTableModel tbm = (DefaultTableModel) displayTabl.getModel();
        int index = displayTabl.getSelectedRow();
        id = Integer.parseInt(tbm.getValueAt(index, 0).toString());
        try {
            Registry rg = LocateRegistry.getRegistry("127.0.0.1", 81);
            EleaderInterface infc = (EleaderInterface) rg.lookup("eleader");
            Eleader eleader = new Eleader();
            eleader.setEleaderId(id);
            Eleader bishp = infc.searchById(eleader);
            if (!bishp.equals(null)) {
                nameTxt.setText(bishp.getEleaderNames());
                churchTxt.setText(bishp.getEleaderChurch());
                passwordTxt.setText(bishp.getEleaderPassword());
                emailTxt.setText(bishp.getEleaderEmail());
                telephoneTxt.setText(bishp.getEleaderTelphone());
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_displayTablMouseClicked

