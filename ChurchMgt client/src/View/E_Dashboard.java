    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (nameTxt.getText().trim().isEmpty() || churchTxt.getText().trim().isEmpty() || telephoneTxt.getText().trim().isEmpty() || emailTxt.getText().trim().isEmpty() || passwordTxt.getText().trim().isEmpty() || re_passworTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Plz provide all DAta!!");
        } else if (!telephoneTxt.getText().matches(numberPtn)) {
            JOptionPane.showMessageDialog(this, "Letters not allowed in phone number");
        } else if (telephoneTxt.getText().length() != 10) {
            JOptionPane.showMessageDialog(this, "phone number must be 10 number");
        } else if (!telephoneTxt.getText().substring(0, 2).equals("07")) {
            JOptionPane.showMessageDialog(this, "phone number must start with 07*******");
        } else if (passwordTxt.getText().length() < 8) {
            JOptionPane.showMessageDialog(this, "password can be like 8 or above characters");
        } else {
            try {
                Registry rg = LocateRegistry.getRegistry("127.0.0.1", 81);
                EleaderInterface infc = (EleaderInterface) rg.lookup("eleader");
                Eleader eleader = new Eleader();
                eleader.setEleaderNames(nameTxt.getText());
                eleader.setEleaderPassword(passwordTxt.getText());
                eleader.setEleaderEmail(emailTxt.getText());
                eleader.setEleaderTelphone(telephoneTxt.getText());
               eleader.setEleaderChurch(churchTxt.getText());
                String Fedback = infc.addEleader(eleader);
                if (!Fedback.isEmpty()) {
                    JOptionPane.showMessageDialog(this, Fedback);
                    tbm.setRowCount(0);
                    addrows();
                    churchTxt.setText(null);
                    nameTxt.setText(null);
                    emailTxt.setText(null);
                    passwordTxt.setText(null);
                    telephoneTxt.setText(null);

                } else {
                    JOptionPane.showMessageDialog(this, "nost saved");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

